     
package Matematika;

public class Matematika {
     int a, b;
    
    Matematika(int a, int b){
        this.a = a;
        this.b = b;
    }   

    public   int pertambahan(){
        return a + b;
    }
    
    public  int pengurangan(){
        return a - b;
    }
    
    public  int perkalian(){
        return a * b;
    }
    
    public  int pembagian(){
        return a / b;
    }
    
}
